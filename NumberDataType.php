<?php

// integer
echo "Decimal : ";
var_dump(1234);

echo "Octal : ";
var_dump(0123);

echo "Hexadecimal : ";
var_dump(0x1A);

echo "Binary : ";
var_dump(0b111111);

echo "Underscore in number : ";
var_dump(1_123_412_341);

// floating point

echo "Floating Point : ";
var_dump(1.234);

echo "Floating Point with E Notation Plus (1.2 x 1000) : ";
var_dump(1.2e3);

echo "Floating Point with E Notation Minus (1.2 x 0.001) : ";
var_dump(1.2e-3);

echo "Underscore in floating point : ";
var_dump(1_234_234_234.12);